A = "10010011001010011001010010000110101001010001010110100111010101010101011010010101011000111100110100111010110100101010110100101000101011101101011101011010"
B = "11100111010000011111000110100110110101010111010011010100001001100010000111111010000100011010100100011010101110111101111000001000110011001011111000110100"

result = ''

for i in range(0, len(A)):
    if A[i] == B[i]:
        result += '0'
    else:
        result += '1'

# 8 bit씩 끊어 ASCII 코드로 변환
for i in range(0, int(len(result) / 8)):
    print(chr(int(result[i * 8 : (i + 1) * 8], 2)), end='')